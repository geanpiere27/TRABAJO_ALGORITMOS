# Escribir un programa que pida al usuario una palabra y la muestre por pantalla 10 veces
palabra = input("Por favor, ingresa una palabra: ")

for _ in range(10):
    print(palabra)


#Este programa solicita al usuario que ingrese una palabra. Luego, utiliza un bucle for para imprimir la palabra ingresada por el usuario 10 veces.
# La variable _ se usa como una convención para indicar que no se usa el valor de la variable en el bucle,
# solo se ejecuta el bucle un número fijo de veces.